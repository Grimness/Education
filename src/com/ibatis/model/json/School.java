package com.ibatis.model.json;

public class School {
private int fid;
private int farea;
private String fname;
public int getFid() {
	return fid;
}
public void setFid(int fid) {
	this.fid = fid;
}
public int getFarea() {
	return farea;
}
public void setFarea(int farea) {
	this.farea = farea;
}
public String getFname() {
	return fname;
}
public void setFname(String fname) {
	this.fname = fname;
}
}
